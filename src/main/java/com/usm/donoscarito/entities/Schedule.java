package com.usm.donoscarito.entities;

public class Schedule {
	private int idField;
	private int idStateField;
	private int idFieldType;
	private int value;
	
	public int getIdField() {
		return idField;
	}
	public void setIdField(int idField) {
		this.idField = idField;
	}
	public int getIdStateField() {
		return idStateField;
	}
	public void setIdStateField(int idStateField) {
		this.idStateField = idStateField;
	}
	public int getIdFieldType() {
		return idFieldType;
	}
	public void setIdFieldType(int idFieldType) {
		this.idFieldType = idFieldType;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
} 
